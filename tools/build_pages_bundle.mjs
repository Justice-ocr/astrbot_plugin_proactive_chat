import fs from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const pageRoot = path.join(root, "pages", "proactive-chat");
const sourceFiles = [
  "js/utils/auth.js",
  "js/utils/http.js",
  "js/utils/formatters.js",
  "js/utils/markdown.js",
  "js/context/AppContext.jsx",
  "js/hooks/useApi.js",
  "js/hooks/useWebSocket.js",
  "js/components/layout/Sidebar.jsx",
  "js/components/layout/Header.jsx",
  "js/views/StatusView.jsx",
  "js/components/config/ConfigRenderer.jsx",
  "js/views/ConfigView.jsx",
  "js/views/TasksView.jsx",
  "js/views/NotificationsView.jsx",
  "js/views/MarkdownDocsView.jsx",
  "js/app.jsx",
];

async function loadBabel() {
  const url = "https://unpkg.com/@babel/standalone@7.25.6/babel.min.js";
  const response = await fetch(url);
  if (!response.ok) {
    throw new Error(`Failed to download Babel standalone: ${response.status}`);
  }
  const code = await response.text();
  const g = globalThis;
  g.window = g;
  g.self = g;
  (0, eval)(code);
  if (!g.Babel || typeof g.Babel.transform !== "function") {
    throw new Error("Babel standalone did not expose Babel.transform");
  }
  return g.Babel;
}

const Babel = await loadBabel();
const chunks = [
  "/* Generated by tools/build_pages_bundle.mjs. Do not edit by hand. */",
  '"use strict";',
];

for (const relativePath of sourceFiles) {
  const absolutePath = path.join(pageRoot, relativePath);
  const source = await fs.readFile(absolutePath, "utf8");
  const transformed = Babel.transform(source, {
    filename: relativePath,
    presets: [["react", { runtime: "classic" }]],
    sourceType: "script",
    comments: true,
  }).code;
  chunks.push(`\n/* ${relativePath} */\n${transformed.trimEnd()}\n`);
}

const bundle = `${chunks.join("\n").trimEnd()}\n`;
await fs.writeFile(path.join(pageRoot, "app.js"), bundle, "utf8");
await fs.writeFile(path.join(pageRoot, "app.bundle.js"), bundle, "utf8");
